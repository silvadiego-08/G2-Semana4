#Una persona recibe un préstamo de 10,000.00 de un banco y desea saber cuánto pagará de interés, si el banco le cobra una tasa del 27% anual.


prestamo = 10000.00
tasa_interes = 0.27
interes = prestamo * tasa_interes
interes_total = prestamo + interes  

print("Una persona recibe un préstamo de 10,000.00 de un banco y desea saber cuánto pagará de interés, si el banco le cobra una tasa del 27% anual.")
print("El interés que pagará es: ", interes_total)
